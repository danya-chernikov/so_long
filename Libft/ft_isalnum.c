#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c > 0x40 && c < 0x5B) || (c > 0x60 && c < 0x7B))
		return (1);
	if (c > 0x2F && c < 0x3A)
		return (1);
	return (0);
}
